    /**
     * Handle the user "updated" event.
     *
     * @param  \App\User  $user
     * @return void
     */
    public function updated(User $user)
    {
        if (!$user->contratistas->isEmpty()) {
            $contratista = $user->contratistas[0];
            if ($user->role->name == 'contratista'){
                $contratista->estado = 'aprobado';
                $contratista->save();
            } elseif ($user->role->name == 'user'){
                $contratista->estado = 'rechazado';
                $contratista->save();
            }
        }
    }

